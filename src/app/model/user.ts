export default class User {
    
    name:string
    age:Number
    gender:string
    username:string
    password:String

    constructor(name:string, age:Number, gender:string, username:string, password:String){
        
        this.name = name
        this.age= age
        this.gender=gender
        this.username=username
        this.password=password
    }
}
